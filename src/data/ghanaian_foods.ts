export function searchFoods(query: string, limit = 20): FoodItem[] {
  const q = query.toLowerCase().trim();
  if (!q) return GHANAIAN_FOODS.slice(0, limit);
  return GHANAIAN_FOODS.filter(
    (f) =>
      f.name.toLowerCase().includes(q) ||
      f.name_local?.toLowerCase().includes(q) ||
      f.category.toLowerCase().includes(q)
  ).slice(0, limit);
}

export function findFoodByName(name: string): FoodItem | undefined {
  const n = name.toLowerCase();
  return GHANAIAN_FOODS.find(
    (f) => f.name.toLowerCase().includes(n) || f.name_local?.toLowerCase().includes(n)
  );
}

export function calcNutrition(food: FoodItem, portion_g: number) {
  const factor = portion_g / 100;
  return {
    calories: Math.round(food.calories_per_100g * factor),
    protein_g: Math.round(food.protein_per_100g * factor * 10) / 10,
    carbs_g: Math.round(food.carbs_per_100g * factor * 10) / 10,
    fat_g: Math.round(food.fat_per_100g * factor * 10) / 10,
  };
}
